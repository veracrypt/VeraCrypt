	f1 = ks->mk_tab[0][(x1 >> 24) & 0xFF] ^ ks->mk_tab[1][x1 & 0xFF] ^ ks->mk_tab[2][(x1 >> 8) & 0xFF] ^ ks->mk_tab[3][(x1 >> 16) & 0xFF]; \
	f0 += f1; \
	f1 += f0 + rk[2 * (r) + 9]; \
	f0 += rk[2 * (r) + 8];

#define ROUNDA(r) \
	ROUNDT(x0, x1, r) \
	x2 = rotr32(x2 ^ f0, 1); \
	x3 = rotl32(x3, 1) ^ f1;
	
#define ROUNDB(r) \
	ROUNDT(x2, x3, r) \
	x0 = rotr32(x0 ^ f0, 1); \
	x1 = rotl32(x1, 1) ^ f1;

#define RROUNDA(r) \
	ROUNDT(x0, x1, r) \
	x2 = rotl32(x2, 1) ^ f0; \
	x3 = rotr32(x3 ^ f1, 1);

#define RROUNDB(r) \
	ROUNDT(x2, x3, r) \
	x0 = rotl32(x0, 1) ^ f0; \
	x1 = rotr32(x1 ^ f1, 1);


