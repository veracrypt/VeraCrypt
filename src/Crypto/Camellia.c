	uint64 r = bswap_64(*((uint64*)(in + 8))) ^ ks[1];

#ifdef CPPCRYPTO_DEBUG
	printf("r0: %016I64x %016I64x\n", l, r);
#endif
	// r = 1
	uint64 lp = l;
	l = r ^ F(l, ks[2]);
